/**

    文档注释

  public：关键字，公共的，共有的

  class：关键字，类别（分类）

  HelloWorld：自定义的字名，表示一个类名

  {} 表示范围（作用域）

  /*

  多行注释

  static：关键字  表示静态的

  void：关键字，返回值类型（空，没有返回值）

  main：表示主函数名称

  main后面的（） 表示函数的参数列表

  String[] ：表示字符串数组

  args：表示字符串数组的名称
*/


  
             
  public class HelloWorld {

    public static void main(String[] args) {

      //单行注释   向控制台输出一句话

      //一个分号表示一个语句

      System.out.println("hello world!!");

    }

  }

